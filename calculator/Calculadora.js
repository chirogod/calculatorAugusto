/* creamos una clase que tenga las 4 funciones 
de la calculadora: + - x / */
class Calculadora{
    sumar(num1, num2){
        return num1 + num2
    }
    restar(num1, num2){
        return num1 - num2
    }
    multiplicar(num1,num2){
        return num1 * num2
    }
    dividir(num1,num2){
        return num1 / num2
    }
}